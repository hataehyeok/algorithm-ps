#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(0); cin.tie(0);
    int n; cin >> n;
    int board[1001][1001] = {};
    for (int k = 1; k <= n; ++k) {
        int x, y, w, h; cin >> x >> y >> w >> h;
        for (int i = x; i < x + w; ++i)
            for (int j = y; j < y + h; ++j)
                board[i][j] = k;
    }
    vector<int> area(n + 1);
    for (int i = 0; i <= 1000; ++i)
        for (int j = 0; j <= 1000; ++j)
            if (board[i][j]) area[board[i][j]]++;
    for (int i = 1; i <= n; ++i) cout << area[i] << '\n';
}

